	// free pages!

	size_t i;

	uint32_t range1 = npages_basemem;
	uint32_t range2 = (uint32_t)ROUNDUP((char *)pages + npages*sizeof(struct Page) - KERNBASE, PGSIZE) / PGSIZE;
