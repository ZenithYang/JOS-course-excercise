//
// Allocates a physical page.  If (alloc_flags & ALLOC_ZERO), fills the entire
// returned physical page with '\0' bytes.  Does NOT increment the reference
// count of the page - the caller must do these if necessary (either explicitly
// or via page_insert).
//
// Returns NULL if out of free memory.
//
// Hint: use page2kva and memset
struct Page *
page_alloc(int alloc_flags)
{
	// Fill this function in
	struct Page *result;
	result = page_free_list;
	if(result){
		page_free_list = page_free_list->pp_link;
		if(alloc_flags & ALLOC_ZERO){
			//Need to judge it, otherwise assert in check_page_allocwould fail
			char *tmp = page2kva(result);
			memset(tmp, 0, PGSIZE);
		}
		//result->pp_ref++; //shouldn't do it in page_alloc(), but rather do it in the caller
		//result->pp_link = NULL; //should break the chain		
		return result;
	}
	return NULL;
}

void set_n_zeros(struct Page *pp, int n, int alloc_flags)
{
	if(n <= 0)
		return;

	size_t i;
	struct Page *tmp = pp;
	for(i = 0; i < n; ++i){
		if(alloc_flags & ALLOC_ZERO){
			memset(page2kva(tmp), 0, PGSIZE);
		}
		tmp->pp_ref++;
		tmp = tmp->pp_link;
	}
}

static struct Page* find_last(struct Page* pp, int n)
{
	struct Page *result = pp;
	size_t i;

	for(i = 0; i < n; ++i)
	{
		if(i < n - 1)
			result = result->pp_link;
	}
	return result;
}

//
// Allocates n continuous physical page. If (alloc_flags & ALLOC_ZERO), fills the n pages 
// returned physical page with '\0' bytes.  Does NOT increment the reference
// count of the page - the caller must do these if necessary (either explicitly
// or via page_insert). 
//
// In order to figure out the n pages when return it. 
// These n pages should be organized as a list.
//
// Returns NULL if out of free memory.
// Returns NULL if n <= 0
//
// Try to reuse the pages cached in the chuck list
//
// Hint: use page2kva and memset
struct Page *
page_alloc_npages(int alloc_flags, int n)
{
	// Fill this function

	
	//The simplest way is to brute-force
	struct Page *ctrl = NULL;
	struct Page *prev = page_free_list, *next = prev;
	size_t i;

	if(n <= 0)
		return NULL;
	
	while(NULL != next)
	{
		next = prev->pp_link;
		if(check_continuous(prev, n) && check_inuse(prev, n))
		{
			if(ctrl == NULL){ //means prev is at the top
				page_free_list = prev;
			} else { //Otherwise need to delete it from free_list
				ctrl->pp_link = find_last(prev, n)->pp_link;
			}
			set_n_zeros(prev, n, alloc_flags);
			return prev;
			
		}
		ctrl = prev;
		prev = next;
	}
	
	return NULL;
}

// Return n continuous pages to chunk list. Do the following things:
//	1. Check whether the n pages int the list are continue, Return -1 on Error
//	2. Add the pages to the chunk list
//	
//	Return 0 if everything ok
int
page_free_npages(struct Page *pp, int n)
{
	// Fill this function
	if(n <= 0 || NULL == pp)
	{
		return -1;
	}

	assert(check_continuous(pp, n));

	struct Page *tmp_prev = pp, *tmp = pp; 
	int i;
	for( i = 0; i < n; ++i )
	{
		tmp = tmp->pp_link;
		tmp_prev->pp_ref--;
		if(n - 1 == i)
			break;
		tmp_prev = tmp;
	}

	tmp_prev->pp_link = page_free_list;
	page_free_list = pp;
	
	return 0;
}

//
// Return a page to the free list.
// (This function should only be called when pp->pp_ref reaches 0.)
//
void
page_free(struct Page *pp)
{
	// Fill this function in
	if(pp->pp_ref > 0){
		cprintf("[.................]: pp=%p, pa=%p, ref=%d\n", pp, page2pa(pp), pp->pp_ref);
		panic("Page not free");
	}
	// JUst add it to the head of free_list
	pp->pp_link = page_free_list;
	page_free_list = pp;
}

static int check_inuse(struct Page *pp, int n)
{
	struct Page *tmp = pp;
	size_t i;
	for(i = 0; i < n; ++i){
		if(tmp->pp_ref > 0)
			return 0; //not free
		tmp = tmp->pp_link;
	}
	return 1; //All are free
}

//
// Return new_n continuous pages based on the allocated old_n pages.
// You can man realloc for better understanding.
// (Try to reuse the allocated pages as many as possible.)
//
struct Page *
page_realloc_npages(struct Page *pp, int old_n, int new_n)
{
	// Fill this function
	if(old_n < 0 || new_n < 0) //Check number
		return NULL;

	if(NULL == pp){ //Check if NULL
		if(old_n == 0 && new_n > 0)
			return page_alloc_npages(ALLOC_ZERO, new_n);
		return NULL;
	}

	if(new_n == old_n){
		return pp;
	} else if(new_n < old_n){
		//Just free the last (old_n - new_n) pages
		size_t i;
		struct Page *tmp = pp;
		for(i = 0; i < new_n; ++i)
			tmp = tmp->pp_link;
		//cprintf("[page_realloc_pages]: Branch 2 %p (%d)\n", tmp, old_n - new_n);
		page_free_npages(tmp, old_n - new_n);
		return pp;
	} else {
		//most complicated part
		size_t i;
		struct Page *prev = pp, *tmp = pp;
		for(i = 0; i < old_n; ++i){
			// find last Page
			//cprintf("i == %d, tmp == %p\n", i, tmp);
			tmp = tmp->pp_link;
			if(i < old_n - 1)
				prev = tmp;
		}
		//cprintf("[page_realloc_pages]: Branch 3 %p (%d)\n", tmp, old_n, new_n);
		//cprintf("[%d %d %d]\n", check_continuous(tmp, new_n - old_n), check_inuse(tmp, new_n - old_n), page2pa(tmp) - page2pa(prev));
		if(check_continuous(tmp, new_n - old_n) && check_inuse(tmp, new_n - old_n) && (page2pa(tmp) - page2pa(prev) == PGSIZE))
		{
			//cprintf("[page_realloc_pages]: Branch 4 %p (%d, %d)\n", tmp, old_n, new_n);
			set_n_zeros(tmp, new_n - old_n, ALLOC_ZERO);
			page_free_list = find_last(tmp, new_n - old_n)->pp_link;
			return pp;
		} else {
			// allocate a whole new area and then free the original Pages
			//cprintf("[page_realloc_pages]: Branch 5 (%d, %d)\n", old_n, new_n);
			struct Page *result = page_alloc_npages(ALLOC_ZERO, new_n);
			//cprintf("[page_realloc_pages]: Branch 5-1 %p\n", result);

			memmove(page2kva(result), page2kva(pp), old_n*PGSIZE);
			page_free_npages(pp, old_n);
			return result;
		}
	}
}

//
// Decrement the reference count on a page,
// freeing it if there are no more refs.
//
void
page_decref(struct Page* pp)
{
	if (--pp->pp_ref == 0)
		page_free(pp);
}

